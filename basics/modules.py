"""module name: math
    DESCRIPTION:
    This module provides access to the mathematical functions
    defined by the C standard."""


import math
print(math.sqrt(4))
print(math.fabs(-5))
print(math.floor(4.6))
print(math.ceil(4.6))
print(math.pow(2, 2))
print(math.trunc(7.999))
print(math.factorial(7))
print(math.tan(2.1))
print(math.pi)
print(math.tau)
print(math.e)
print("Positive Infinity-", math.inf)

