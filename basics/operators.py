# Arithmatic Operators

x=6
y=5
print(x+y)
print(x-y)
print(x*y)
print(x/y)


# Relational Operators
print(x==y)
print(x>=y)
print(y>=x)
print(y>x)

print(x!=y)
print(y!=x)

s,p = 8,8           # we can assign values in this way for two variables
print(s!=p)


# Logical Operators(and,or,not)
a=5
b=4
print(a<8 and b<5)
print(a<8 and b<2)
print(a<8 or b<2)

x=True
print(x)
print(not x)

